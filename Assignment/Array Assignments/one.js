// How to Create Array in JavaScript? 

let Products = ["One-Plus", "I-Phone", "Samsung", "Xiami-Redmi", "VIVO", "OPPO"]

// How to Access Items from Array in JavaScript?

console.log(Products)

// How to Access Last Item from Array in JavaScript? 

console.log(Products[5])

// How to Access All Items from Array Using (for Loop) in JavaScript?

for (var i = 0; i <= Products.length - 1; i = i + 1) {
    console.log(Products[i])
}
