// JavaScript Array Push Method?

let Cars = ["Volvo", "BMW", "Audi", "Kia", "Tesla"]

console.log(Cars)

Cars.push("Mercedez")
console.log(Cars)

// JavaScript Array Pop Method?

let End = Cars.pop()
console.log(Cars)
console.log(End)

// JavaScript Array Unshift Method?

Cars.unshift("Bugatti")
console.log(Cars)

// JavaScript Array Shift Method?

let Start = Cars.shift()
console.log(Cars)
console.log(Start)